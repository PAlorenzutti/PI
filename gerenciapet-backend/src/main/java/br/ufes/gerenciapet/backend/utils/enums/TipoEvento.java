package br.ufes.gerenciapet.backend.utils.enums;

/**
 * Categorias de eventos ofertados pelos grupos PET.
 */
public enum TipoEvento {
    /** Evento prático de curta duração. */
    WORKSHOP,
    /** Curso estruturado com carga horária definida. */
    CURSO,
    /** Palestra ou apresentação temática. */
    PALESTRA,
    /** Evento genérico que não se enquadra nas demais categorias. */
    EVENTO
}
